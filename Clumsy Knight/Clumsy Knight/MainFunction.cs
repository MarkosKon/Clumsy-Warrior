    static class RectangleHelper
    {
        const int penetrationMargin = 5;

        public static bool isOnTopOf(this Rectangle r1, Rectangle r2)
        {
            return (r1.Bottom <= r2.Top + 5 &&
                r1.Bottom >= r2.Top - 5 &&
                r1.Right >= r2.Left &&
                r1.Left <= r2.Right);
        }
